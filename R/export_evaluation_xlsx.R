#' Export external evaluation results to Excel
#'
#' Exports pharmacokinetic external evaluation results to an Excel workbook
#' (`.xlsx`) using \pkg{openxlsx}.
#'
#' Supported inputs include:
#' \itemize{
#'   \item Objects of class \code{EvalPPK}, typically returned by [exeval_ppk()]
#'   \item Objects of class \code{EvalMetricsPPK}, typically returned by [metrics_occ()]
#'   \item Observation-level metrics data frames
#' }
#'
#' Depending on the input, the workbook may include:
#'
#' \itemize{
#'   \item Summary metrics by occasion
#'   \item Observation-level metrics
#'   \item Fit classification results from [screen_fit()]
#'   \item Metadata describing the evaluation
#' }
#'
#' @param x Either:
#' \itemize{
#'   \item An object of class \code{EvalPPK}
#'   \item An object of class \code{EvalMetricsPPK}
#'   \item A data frame containing observation-level prediction metrics
#' }
#' @param file Character string. Name or path of the Excel file to create.
#' Default is \code{"external_evaluation_results.xlsx"}.
#' @param include_screen_fit Logical. If \code{TRUE}, includes an additional
#' worksheet with fit classification results generated by [screen_fit()].
#' @param fit_classes Character vector specifying which fit categories to include
#' in the screen fit worksheet. Passed to [screen_fit()].
#' Default is \code{c("Excellent","Acceptable","Poor", "Very Poor")}.
#' @param occ Optional numeric occasion filter passed to [screen_fit()].
#' If \code{NULL}, all occasions are considered.
#'
#' @return Invisibly returns the path to the exported Excel file.
#'
#' @details
#' The generated workbook may contain up to four worksheets:
#'
#' \describe{
#'   \item{Summary Metrics}{Aggregated performance metrics by occasion.}
#'   \item{Observation Metrics}{Observation-level prediction errors and metrics.}
#'   \item{Screen Fit}{Fit classification results generated with [screen_fit()].}
#'   \item{Metadata}{Evaluation metadata describing the performed analysis.}
#' }
#'
#' If a plain data frame is provided, only the observation-level metrics
#' worksheet is exported unless screening is explicitly requested and the
#' required columns are available.
#'
#' @examples
#' \dontrun{
#' # Export results from a full evaluation workflow
#' res <- exeval_ppk(...)
#' export_evaluation_xlsx(res)
#'
#' # Export results from metrics object
#' mets <- metrics_occ(...)
#' export_evaluation_xlsx(mets)
#'
#' # Export only observation-level metrics
#' export_evaluation_xlsx(mets$metrics)
#'
#' # Export selected fit categories for OCC 2
#' export_evaluation_xlsx(
#'   res,
#'   file = "filtered_results.xlsx",
#'   fit_classes = c("Excellent", "Acceptable"),
#'   occ = 2
#' )
#' }
#'
#' @export

export_evaluation_xlsx <- function(x,
                                   file = "external_evaluation_results.xlsx",
                                   include_screen_fit = TRUE,
                                   fit_classes = c("Excellent","Acceptable", "Poor", "Very Poor"),
                                   occ = NULL) {

  metadata <- NULL
  metrics_means <- NULL

  if (!requireNamespace("openxlsx", quietly = TRUE)) {
    stop("Package 'openxlsx' is required.")
  }

  # -----------------------------
  # EvalPPK
  # -----------------------------
  if (inherits(x, "EvalPPK")) {

    metrics_obj <- x$metrics
    metrics_obs <- metrics_obj$metrics
    metrics_means <- metrics_obj$metrics_means
    metadata <- attr(x, "attributes")
    colnames(metadata) <- c("Parameter", "Value")
  }

  # -----------------------------
  # EvalMetricsPPK
  # -----------------------------
  else if (inherits(x, "EvalMetricsPPK")) {

    metrics_obj <- x
    metrics_obs <- x$metrics
    metrics_means <- x$metrics_means

    metadata <- data.frame(
      Parameter = c(
        "Evaluation type",
        "Assessment",
        "Tool"
      ),
      Value = c(
        attr(x, "eval_type"),
        attr(x, "assessment"),
        attr(x, "tool")
      )
    )
  }

  # -----------------------------
  # data.frame
  # -----------------------------
  else if (is.data.frame(x)) {

    metrics_obj <- x
    metrics_obs <- x

  }

  else {
    stop(
      "'x' must be an EvalPPK, EvalMetricsPPK, or data.frame."
    )
  }

  wb <- openxlsx::createWorkbook()

  # Summary metrics
  if (!is.null(metrics_means)) {

    openxlsx::addWorksheet(
      wb,
      "Summary Metrics"
    )

    openxlsx::writeData(
      wb,
      sheet = "Summary Metrics",
      x = metrics_means
    )
  }

  # Observation metrics
  openxlsx::addWorksheet(
    wb,
    "Observation Metrics"
  )

  openxlsx::writeData(
    wb,
    sheet = "Observation Metrics",
    x = metrics_obs
  )

  # Screen fit
  if (include_screen_fit) {

    screened <- screen_fit(
      metrics = metrics_obj,
      occ = occ,
      fit_classes = fit_classes
    )

    if (nrow(screened) > 0) {

      openxlsx::addWorksheet(
        wb,
        "Screen Fit"
      )

      openxlsx::writeData(
        wb,
        sheet = "Screen Fit",
        x = screened
      )
    }
  }

  # Metadata
  if (!is.null(metadata)) {

    openxlsx::addWorksheet(
      wb,
      "Metadata"
    )

    openxlsx::writeData(
      wb,
      sheet = "Metadata",
      x = metadata
    )
  }

  openxlsx::saveWorkbook(
    wb,
    file = file,
    overwrite = TRUE
  )

  message(
    "Results exported to: ",
    normalizePath(file, mustWork = FALSE)
  )

  invisible(file)
}
